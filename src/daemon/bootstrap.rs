    /// U2: the automount translation — lowercase drive, slashes flipped,
    /// spaces preserved; UNC and relative paths are untranslatable.
    #[test]
    fn wsl_mnt_path_translation() {
        assert_eq!(
            wsl_mnt_path(Path::new("C:\\A B\\c.bashrc")).as_deref(),
            Some("/mnt/c/A B/c.bashrc")
        );
        assert_eq!(
            wsl_mnt_path(Path::new("d:\\x\\y")).as_deref(),
            Some("/mnt/d/x/y")
        );
        assert_eq!(wsl_mnt_path(Path::new("\\\\server\\share\\f")), None);
        assert_eq!(wsl_mnt_path(Path::new("relative\\f")), None);
        assert_eq!(wsl_mnt_path(Path::new("C:")), None);
    }

    /// U4: template/quoting goldens — token substitution, hook grammar bytes,
    /// restore trailing with an embedded single quote in the cwd.
    #[test]
    fn bash_template_goldens() {
        let body = BASH_TEMPLATE
            .replace("{MOTD_PRELUDE}", "")
            .replace("{TOKEN}", "0123456789abcdef");
        assert!(body.contains("__TC_TOK='0123456789abcdef'"));
        // Login-shell emulation (banner-visibility fix): /etc/profile first,
        // then bash's own login order — first existing of bash_profile /
        // bash_login / profile — with ~/.bashrc strictly a no-profile
        // fallback. The rc replaces a LOGIN shell; --rcfile can't be
        // combined with -l, so the chain lives here.
        let profile_at = body.find("if [ -f /etc/profile ]; then . /etc/profile; fi").unwrap();
        let chain_at = body
            .find("if [ -f ~/.bash_profile ]; then . ~/.bash_profile\nelif [ -f ~/.bash_login ]; then . ~/.bash_login\nelif [ -f ~/.profile ]; then . ~/.profile\nelif [ -f ~/.bashrc ]; then . ~/.bashrc\nfi")
            .unwrap();
        let tok_at = body.find("__TC_TOK=").unwrap();
        assert!(profile_at < chain_at && chain_at < tok_at,
            "login chain precedes hook installation");
        // The hook grammar bytes the scanner pins.
        assert!(body.contains(r"'\033]7717;%s;%s;%s\007'"));
        assert!(body.contains(r"'\033]9;9;%s\007'"));
        // Idempotent per-prompt PS1 wrap: 133;A prefix, 133;B suffix.
        assert!(body.contains(r#"PS1="\[\033]133;A\007\]${PS1}\[\033]133;B\007\]""#));
        assert!(body.contains(r"'\[\033]133;A\007\]'*"));
        // Ours-first (exit-code capture) + arm-LAST (user hooks must not
        // consume/trip the latch) PROMPT_COMMAND wrap.
        assert!(body.contains(
            "PROMPT_COMMAND=\"__tc_pre${PROMPT_COMMAND:+;$PROMPT_COMMAND};__tc_arm\""
        ));
        // One-shot DEBUG-trap latch + the own-plumbing name guard (the ^C
        // spurious-exec fix — probe wsl_composer_semantics pins it live).
        assert!(body.contains("trap '__tc_debug' DEBUG"));
        assert!(body.contains("case \"$BASH_COMMAND\" in __tc_*) return ;; esac"));
        assert!(body.contains("__tc_arm() { __tc_at_prompt=1; }"));
        // No CRLF (bash chokes on \r in constructs).
        assert!(!body.contains('\r'));
    }

    /// U4 (cmd half): the PROMPT value golden — token substitution, the
    /// constant hex pre payload, verb stream order (9;9 → pre → 133;A →
    /// base → 133;B), ST terminators throughout (PROMPT has no BEL macro),
    /// and user-PROMPT wrapping (never replacement).
    #[test]
    fn cmd_prompt_value_goldens() {
        let v = cmd_prompt_value("0123456789abcdef", None);
        assert_eq!(
            v,
            "$E]9;9;$P$E\\$E]7717;0123456789abcdef;pre;7b2265223a6e756c6c2c226e223a307d$E\\$E]133;A$E\\$P$G$E]133;B$E\\"
        );
        // The hex payload decodes to the exact lenient-schema constant.
        let hex = "7b2265223a6e756c6c2c226e223a307d";
        let json: Vec<u8> = hex
            .as_bytes()
            .chunks(2)
            .map(|p| {
                u8::from_str_radix(std::str::from_utf8(p).unwrap(), 16).unwrap()
            })
            .collect();
        assert_eq!(json, br#"{"e":null,"n":0}"#);
        // A customized user PROMPT is wrapped in place of $P$G.
        let v = cmd_prompt_value("t", Some("$D $P$G"));
        assert!(v.contains("$E]133;A$E\\$D $P$G$E]133;B$E\\"));
        assert!(!v.contains("$P$G$E]133;B$E\\$D"), "user prompt replaced, not wrapped");
        // Blank/whitespace user PROMPT falls back to the default.
        assert_eq!(cmd_prompt_value("t", Some("  ")), cmd_prompt_value("t", None));
        // No literal ESC/BEL bytes — everything rides $E macros.
        assert!(!v.contains('\x1b') && !v.contains('\x07'));
    }

    /// What conhost renders from that PROMPT (macros expanded) parses into
    /// exactly the hook events the daemon consumes: a tokenful `pre` with
    /// e=None/n=0/empty cwd (the osc_cwd substitution's trigger) and the
    /// tokenless 133;B prompt-end — and NO exec, ever.
    #[test]
    fn cmd_prompt_rendered_stream_parses() {
        use crate::daemon::blocks::{BlockScanner, HookVerb};
        let v = cmd_prompt_value("0123456789abcdef", None);
        // Macro expansion as cmd performs it: $E → ESC, $P → path, $G → '>',
        // $E\ therefore → ESC \ (ST).
        let rendered = v
            .replace("$E", "\x1b")
            .replace("$P", "C:\\Users\\alice")
            .replace("$G", ">");
        let mut sc = BlockScanner::new();
        let evs = sc.feed(rendered.as_bytes());
        let verbs: Vec<&HookVerb> = evs.iter().map(|e| &e.verb).collect();
        assert_eq!(verbs.len(), 2, "pre + PromptEnd only: {verbs:?}");
        assert_eq!(
            *verbs[0],
            HookVerb::Pre { exit: None, n: 0, cwd: String::new() }
        );
        assert_eq!(evs[0].token, "0123456789abcdef");
        assert_eq!(*verbs[1], HookVerb::PromptEnd);
        // The 9;9 stays a foreign OSC to the block scanner (the session's
        // OscScanner owns it) and 133;A stays ignored.
    }

    /// U4 (ssh half): base64 vectors (RFC 4648 — the remote coreutils
    /// `base64 -d` must accept our bytes verbatim; the probe proves it live).
    #[test]
    fn base64_vectors() {
        assert_eq!(base64_encode(b""), "");
        assert_eq!(base64_encode(b"f"), "Zg==");
        assert_eq!(base64_encode(b"fo"), "Zm8=");
        assert_eq!(base64_encode(b"foo"), "Zm9v");
        assert_eq!(base64_encode(b"foob"), "Zm9vYg==");
        assert_eq!(base64_encode(b"fooba"), "Zm9vYmE=");
        assert_eq!(base64_encode(b"foobar"), "Zm9vYmFy");
        // The alphabet is shell-inert inside single quotes everywhere.
        let all = base64_encode(&(0u8..=255).collect::<Vec<u8>>());
        assert!(all.chars().all(|c| c.is_ascii_alphanumeric() || c == '+' || c == '/' || c == '='));
    }

    /// U4 (ssh half): the remote command string — exact synthesis golden,
    /// single-argv `sh -c '<body>'` shape, and the fish/csh-safe outer
    /// contract (no single quotes, backslashes, or `!` inside the body).
    #[test]
    fn ssh_remote_command_golden() {
        let rc = b"echo rc";
        let cmd = ssh_remote_command(rc);
        assert_eq!(
            cmd,
            "sh -c 'f=$(mktemp); printf %s ZWNobyByYw== | base64 -d > \"$f\" 2>/dev/null; \
             if command -v bash >/dev/null 2>&1; then \
             if [ -s \"$f\" ]; then export TC_RC=\"$f\"; exec bash --rcfile \"$f\" -i; fi; \
             rm -f -- \"$f\"; exec bash -i; fi; \
             rm -f -- \"$f\"; exec sh -i'"
        );
        // Outer-shell safety: exactly the two wrapping single quotes, and no
        // csh-hot or escape-hot bytes anywhere in the body.
        let body = cmd.strip_prefix("sh -c '").unwrap().strip_suffix('\'').unwrap();
        assert!(!body.contains('\'') && !body.contains('!') && !body.contains('\\'));
        // A REAL generated rc keeps those properties too (b64 + template are
        // both inert; the trailing rides INSIDE the b64, never the body).
        let full = BASH_TEMPLATE
            .replace("{MOTD_PRELUDE}", SSH_MOTD_PRELUDE)
            .replace("{TOKEN}", "0123456789abcdef")
            .replace("{RESTORE_TRAILING}", "cd '/a b' 2>/dev/null");
        let cmd = ssh_remote_command(full.as_bytes());
        let body = cmd.strip_prefix("sh -c '").unwrap().strip_suffix('\'').unwrap();
        assert!(!body.contains('\'') && !body.contains('!') && !body.contains('\\'));
    }

    /// U4 (ssh half): the remote rc — self-delete lands between the init emit
    /// and the restore trailing (token leaves /tmp before any resumed CLI
    /// starts), and the ssh trailing forms (best-effort cd; cd+exec-emit+
    /// resume) hit their goldens.
    #[test]
    fn ssh_rcfile_and_trailing_goldens() {
        assert_eq!(ssh_cd_trailing("/home/z"), "cd '/home/z' 2>/dev/null");
        assert_eq!(
            ssh_cd_trailing("/home/o'brien"),
            "cd '/home/o'\\''brien' 2>/dev/null"
        );
        let t = ssh_restore_trailing("/srv/proj", "claude --resume abc-123").unwrap();
        assert_eq!(
            t,
            "cd '/srv/proj' 2>/dev/null; { __tc_emit exec '{\"c\":\"claude --resume abc-123\"}'; claude --resume abc-123; }"
        );
        assert!(ssh_restore_trailing("/x", "weird 'quoted' resume").is_none());
        // Assembled rc body ordering: motd prelude → login chain → … init
        // emit → self-delete → trailing.
        let tail = format!("{SSH_SELF_DELETE}{t}");
        let body = BASH_TEMPLATE
            .replace("{MOTD_PRELUDE}", SSH_MOTD_PRELUDE)
            .replace("{TOKEN}", "t")
            .replace("{RESTORE_TRAILING}", &tail);
        let motd_at = body.find("cat /run/motd.dynamic /etc/motd").unwrap();
        let profile_at = body.find(". /etc/profile").unwrap();
        let init_at = body.find("__tc_emit init").unwrap();
        let del_at = body.find("rm -f -- \"$TC_RC\"").unwrap();
        let trail_at = body.find("cd '/srv/proj'").unwrap();
        assert!(motd_at < profile_at && profile_at < init_at);
        assert!(init_at < del_at && del_at < trail_at);
        assert!(!body.contains('\r'));
        // pam_motd emulation is gated on hushlogin + MOTD_SHOWN, and marks
        // MOTD_SHOWN so profile.d motd scripts never double-print.
        assert!(body.contains("[ ! -f ~/.hushlogin ]"));
        assert!(body.contains("export MOTD_SHOWN=tc"));
    }

    /// Banner-visibility fix (pwsh half): the banner block substitutes at the
    /// TOP of the template — before the token, hooks, and init emit — prints
    /// the byte-verified 5.1 text on the Desktop branch, and disappears
    /// entirely when the spawn opts out.
    #[test]
    fn ps_template_banner_goldens() {
        let with = TEMPLATE
            .replace("{BANNER}", PS_BANNER)
            .replace("{TOKEN}", "0123456789abcdef");
        let banner_at = with.find("Windows PowerShell`r`n").unwrap();
        let tok_at = with.find("$script:TCTok").unwrap();
        let init_at = with.find("TCEmit 'init'").unwrap();
        assert!(banner_at < tok_at && tok_at < init_at, "banner precedes hooks");
        // The exact 5.1 text a real interactive powershell.exe prints
        // (captured on this machine): logo, copyright, blank, hint, blank.
        assert!(with.contains(
            "Windows PowerShell`r`nCopyright (C) Microsoft Corporation. All rights reserved.`r`n`r`nInstall the latest PowerShell for new features and improvements! https://aka.ms/PSWindows`r`n`r`n"
        ));
        // Core prints its one-line version banner instead.
        assert!(with.contains("PowerShell $($PSVersionTable.PSVersion)`r`n"));
        // Opt-out leaves no banner residue.
        let without = TEMPLATE
            .replace("{BANNER}", "")
            .replace("{TOKEN}", "0123456789abcdef");
        assert!(!without.contains("Windows PowerShell"));
        assert!(without.starts_with("# generated by Pulse"));
    }

    /// `-NoLogo` honor: any unambiguous prefix (PowerShell's own flag
